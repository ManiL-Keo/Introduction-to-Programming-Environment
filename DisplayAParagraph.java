public class DisplayAParagraph {
    public static void main(String[] args) {
        System.out.println("\\n                                        Line Break.");
        System.out.println("\\t                                        Tabulation.");
        System.out.println("\\’                                      Single Quote.");
        System.out.println("\\”                                      Double Quote.");
        System.out.println("\\\\                                          \\ Sign.");
        System.out.println("\\\\\\\\                                    \\\\ Sign.");
        System.out.println("//                                       Line Comment.");
        System.out.println("/* ... */                               Block Comment.");
        System.out.println("\"\"\"\n                                   Text block.\n\"\"\"");
    }
}
